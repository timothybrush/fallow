async function renderPackageJson(schema) {
  const { issueTypeDefaults } = issueRegistryFromCapabilitySchema(schema);
  const pkg = JSON.parse(await readFile(PACKAGE_JSON_PATH, "utf8"));
  const issueTypes =
    pkg?.contributes?.configuration?.properties?.["fallow.issueTypes"];
  if (!issueTypes || typeof issueTypes !== "object") {
    throw new Error("editors/vscode/package.json is missing fallow.issueTypes configuration");
  }

  issueTypes.default = issueTypeDefaults;
  issueTypes.properties = Object.fromEntries(
    Object.keys(issueTypeDefaults).map((key) => [key, { type: "boolean" }]),
  );

  return `${JSON.stringify(pkg, null, 2)}\n`;
}

/**
 * Append aliases for serde-flattened inner types that jstt dedupes (see
 * `FLATTEN_DEDUPED_ALIASES` for the full rationale and extension recipe).
 *
 * When `wrapperOnlyFields` is empty, emits `export type Name = Parent` (a
 * full alias) instead of `export type Name = Omit<Parent, ...>` (an Omit
 * alias with no fields to omit). Used for bare types whose property set is
 * BYTE-IDENTICAL to the wrapper on the wire (e.g., the pre-wrapper
 * `DuplicationReport` whose `clone_groups[]` items always carried injected
 * `actions[]` even before the typed wrapper migration).
 */
function appendDedupedFlattenAliases(contents, deadCodeAliases) {
  let out = contents;
  let backwardsCompatHeaderEmitted = false;
  for (const alias of FLATTEN_DEDUPED_ALIASES) {
    const isBackwardsCompat = alias.description.startsWith(
      "Backwards-compat alias",
    );
    if (isBackwardsCompat && !backwardsCompatHeaderEmitted) {
      out += BACKWARDS_COMPAT_ALIASES_HEADER;
      backwardsCompatHeaderEmitted = true;
    }
    const rhs = alias.wrapperOnlyFields.length === 0
      ? alias.parent
      : `Omit<${alias.parent}, ${alias.wrapperOnlyFields
          .map((field) => `"${field}"`)
          .join(" | ")}>`;
    out += `\n/**\n * ${alias.description}\n */\nexport type ${alias.name} = ${rhs};\n`;
  }
  // Emit the schema-derived dead-code aliases under the same
  // "Backwards-compat aliases" section header. If FLATTEN_DEDUPED_ALIASES had
  // no backwards-compat entries, drop the header here so the section still gets
  // one explanatory block before the first dead-code alias.
  if (!backwardsCompatHeaderEmitted) {
    out += BACKWARDS_COMPAT_ALIASES_HEADER;
    backwardsCompatHeaderEmitted = true;
  }
  for (const alias of deadCodeAliases.singles) {
    const description = bareDeadCodeAliasDescription(alias.name, alias.parent);
    out += `\n/**\n * ${description}\n */\nexport type ${alias.name} = ${alias.parent};\n`;
  }
  for (const alias of deadCodeAliases.unions) {
    const description = bareDeadCodeUnionAliasDescription(
      alias.name,
      alias.parents,
    );
    out += `\n/**\n * ${description}\n */\nexport type ${alias.name} = ${alias.parents.join(" | ")};\n`;
  }
  return out;
}

/**
 * Verify each `FLATTEN_DEDUPED_ALIASES` entry actually landed in the
 * generated `.d.ts`, AND that its `parent` interface exists (otherwise the
 * `Omit<Parent, ...>` alias would not resolve at consumer type-check time).
 *
 * Throws if either invariant is violated. Catches three drift scenarios:
 *   1. `appendDedupedFlattenAliases` regressed (e.g. typo in the emit
 *      template) and the alias is missing from the output.
 *   2. The parent type was renamed in the schema (e.g. `HealthFinding` to
 *      `HealthFindingEnvelope`) but the alias entry still references the
 *      old name. The alias would emit `Omit<<old-name>, ...>` and break.
 *   3. The parent type got dedupe-suppressed itself (e.g. someone wrapped
 *      `HealthFinding` in another `#[serde(flatten)]` wrapper). The
 *      alias would silently resolve to an unknown type.
 *
 * Future-proofing note: if jstt starts emitting the inner natively (e.g. a
 * version bump that disables the subsume-dedupe heuristic), the alias and
 * the natively-emitted interface would conflict on a duplicate identifier
 * and `pnpm run lint` (tsc --noEmit) would fail; this assertion is
 * deliberately narrower so the failure surfaces at codegen time first.
 */
function assertAliasesEmitted(contents, deadCodeAliases) {
  const assertAlias = (name, parents, table) => {
    const aliasRe = new RegExp(`^export type ${name}\\b`, "m");
    if (!aliasRe.test(contents)) {
      throw new Error(
        `assertAliasesEmitted: expected \`export type ${name}\` in generated output. ` +
          `If jstt now emits this type natively or it was renamed, update ${table}.`,
      );
    }
    for (const parent of parents) {
      const parentRe = new RegExp(`^export interface ${parent}\\b`, "m");
      if (!parentRe.test(contents)) {
        throw new Error(
          `assertAliasesEmitted: expected \`export interface ${parent}\` (parent of ${name}) in generated output. ` +
            `The alias for \`${name}\` would fail to resolve without it. ` +
            `Did ${parent} get renamed or dedupe-suppressed?`,
        );
      }
    }
  };
  for (const alias of FLATTEN_DEDUPED_ALIASES) {
    assertAlias(alias.name, [alias.parent], "FLATTEN_DEDUPED_ALIASES");
  }
  for (const alias of deadCodeAliases.singles) {
    assertAlias(alias.name, [alias.parent], "fallow schema ts_alias");
  }
  for (const alias of deadCodeAliases.unions) {
    assertAlias(alias.name, alias.parents, "fallow schema ts_alias");
  }
}

async function generateOutputContract(capabilitySchema) {
  const raw = await readFile(SCHEMA_PATH, "utf8");
  const parsed = JSON.parse(raw);
  const deadCodeAliases = deadCodeAliasesFromCapabilitySchema(capabilitySchema);
  stripTitles(parsed);
  flattenRefSiblings(parsed);
  // With the root title stripped, jstt uses the second arg as the name of the
  // top-level union type.
  const raw_ts = await compile(parsed, "FallowJsonOutput", OPTIONS);
  const version = await readRustSchemaVersion();
  const final = appendDedupedFlattenAliases(
    stripTrailingWhitespace(pinSchemaVersion(raw_ts, version)),
    deadCodeAliases,
  );
  assertAliasesEmitted(final, deadCodeAliases);
  return final;
}

async function generate() {
  const capabilitySchema = await loadCapabilitySchema();
  return {
    outputContract: await generateOutputContract(capabilitySchema),
    issueTypes: renderIssueTypesTs(capabilitySchema),
    lspInitializationOptions: renderLspInitializationOptionsTs(),
    packageJson: await renderPackageJson(capabilitySchema),
  };
}

async function writeAll(artifacts) {
  for (const path of OUTPUT_CONTRACT_PATHS) {
    await mkdir(dirname(path), { recursive: true });
    await writeFile(path, artifacts.outputContract, "utf8");
  }
  await mkdir(dirname(ISSUE_TYPES_PATH), { recursive: true });
  await writeFile(ISSUE_TYPES_PATH, artifacts.issueTypes, "utf8");
  await mkdir(dirname(LSP_INITIALIZATION_OPTIONS_PATH), { recursive: true });
  await writeFile(
    LSP_INITIALIZATION_OPTIONS_PATH,
    artifacts.lspInitializationOptions,
    "utf8",
  );
  await writeFile(PACKAGE_JSON_PATH, artifacts.packageJson, "utf8");
}

async function check() {
  const artifacts = await generate();
  const stale = [];
  const checks = [
    ...OUTPUT_CONTRACT_PATHS.map((path) => [path, artifacts.outputContract]),
    [ISSUE_TYPES_PATH, artifacts.issueTypes],
    [LSP_INITIALIZATION_OPTIONS_PATH, artifacts.lspInitializationOptions],
    [PACKAGE_JSON_PATH, artifacts.packageJson],
  ];
  for (const [path, generated] of checks) {
    const committed = await readFile(path, "utf8").catch(() => "");
    if (committed !== generated) {
      stale.push(path);
    }
  }
  if (stale.length === 0) {
    return 0;
  }
  process.stderr.write(
    "\x1b[31mcheck:contracts FAILED\x1b[0m: the following generated files " +
      "are out of date with `docs/output-schema.json` or `fallow schema`:\n",
  );
  for (const path of stale) {
    process.stderr.write(`  - ${path}\n`);
  }
  process.stderr.write(
    "Run `pnpm --filter fallow-vscode codegen:contracts` and commit the result.\n",
  );
  return 1;
}

const mode = process.argv[2];
if (mode === "--check") {
  const code = await check();
  process.exit(code);
} else {
  const artifacts = await generate();
  await writeAll(artifacts);
  for (const path of [...OUTPUT_CONTRACT_PATHS, ISSUE_TYPES_PATH, PACKAGE_JSON_PATH]) {
    process.stdout.write(`Wrote ${path}\n`);
  }
}
